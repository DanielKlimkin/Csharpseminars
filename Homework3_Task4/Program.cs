// Дано натуральное число в диапазоне от 1 до 100 000. Создайте массив,
// состоящий из цифр этого числа. Старший разряд
// числа должен располагаться на 0-м индексе
// массива, младший – на последнем. Размер массива
// должен быть равен количеству цифр.

Console.WriteLine("Введите натуральное число в диапазоне от 1 до 100000: ");
int number = Convert.ToInt32(Console.ReadLine());
// Проверяем, что число находится в заданном диапазоне
if (number < 1 || number > 100000)
{
    Console.WriteLine("Число должно быть в диапазоне от 1 до 100000.");
    return;
}
// Преобразуем число в строку для работы с цифрами
string numberStr = number.ToString();
int length = numberStr.Length;
// Создаем массив для хранения цифр
int[] array = new int[length];
// Заполняем массив цифрами числа
for (int i = 0; i < array.Length; i++)
{
    array[i] = numberStr[i] - '0'; // Преобразуем символ в цифру
}
Console.WriteLine("Массив цифр числа: [" + string.Join(", ", array) + "]");